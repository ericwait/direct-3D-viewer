function UpdateTimeIndicatorLine(time)
global uiTreeFig uiTreeAx uiTimeLine uiControlHandles useDistance

if (isempty(uiTreeFig) || ~ishandle(uiTreeFig)|| isempty(uiTreeAx) ||~ishandle(uiTreeAx) || isempty(uiControlHandles) ||...
        ~ishandle(uiControlHandles.tb_curFrame)), return, end

if(ishandle(uiTimeLine))
    if (useDistance>0)
        set(uiTimeLine,...
            'XData',        [time time],...
            'YData',        get(uiTreeAx,'YLim'));
    else
        set(uiTimeLine,...
            'YData',        [time time],...
            'XData',        get(uiTreeAx,'XLim'));
    end
else
    if (useDistance>0)
        uiTimeLine = line(...
            [time time],...
            get(uiTreeAx,'YLim'),...
            'color',        'red',...
            'linewidth',    1,...
            'EraseMode',    'xor',...
            'Tag',          'timeIndicatorLine',...
            'Parent',       uiTreeAx);
    else
        uiTimeLine = line(...
            get(uiTreeAx,'XLim'),...
            [time time],...
            'color',        'red',...
            'linewidth',    1,...
            'EraseMode',    'xor',...
            'Tag',          'timeIndicatorLine',...
            'Parent',       uiTreeAx);
    end
end
